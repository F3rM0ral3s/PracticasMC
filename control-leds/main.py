g_led2 = Pin(14, Pin.OUT)
r_led2 = Pin(15, Pin.OUT)
leds = [0, 0, 0, 0, 0, 0, 0, 0]

def on_off():
    r_led1.value(1)
    g_led1.value(1)
    o_led1.value(1)
