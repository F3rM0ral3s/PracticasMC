def shift_right():
    for i in range(0, 8):
        if i == 0:
            leds[i] = int(not leds[i])
            sleep(0.2)
        else:
            leds[i] = int(not leds[i])
            leds[i-1] = int(not leds[i-1])
            sleep(0.2)
        r_led1.value(leds[0])
        g_led1.value(leds[1])
        o_led1.value(leds[2])
        b_led1.value(leds[3])
        b_led2.value(leds[4])
        o_led2.value(leds[5])
        g_led2.value(leds[6])
        r_led2.value(leds[7])

while True:
    shift_right()